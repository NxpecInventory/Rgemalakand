        //
        // POST: /Account/ResetPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ResetPassword(ResetPasswordViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }
            var user = await UserManager.FindByEmailAsync(model.Email);
            if (user == null)
            {
                // Don't reveal that the user does not exist
                ModelState.AddModelError("", "Your Provided Email Is not Registered On RGE Management System Please Enter the Correct and Registered Email To Recover Your Password.");
                return View(model);/*RedirectToAction("ResetPasswordConfirmation", "Account")*/;
            }
            var result = await UserManager.ResetPasswordAsync(user.Id, model.Code, model.Password);
            await UserManager.UpdateAsync(user);
   
            if (result.Succeeded)
            {
                AuthenticationManager.SignOut(DefaultAuthenticationTypes.ApplicationCookie);
                return RedirectToAction("ResetPasswordConfirmation", "Account");
            }
            AddErrors(result);
            return View();
        }

